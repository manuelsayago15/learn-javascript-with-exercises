/*
    Strings
    Exercise 13: Create a function that takes in a string and returns a new string with all the characters in uppercase 
    using the .toUpperCase() method. Example input: "JavaScript", output: "JAVASCRIPT".
*/

function upperCase(string) {
    return string.toUpperCase();
}

console.log(upperCase("javascript"));